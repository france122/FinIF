def main():
    # Load current data
    with open(os.path.join(DEMO_DIR, "benchmark_all.json"), encoding="utf-8") as f:
        benchmark = json.load(f)

    # Build new benchmark
    new_cases = []
    for case in benchmark["cases"]:
        cid = case["case_id"]
        if cid in NEW_QUERIES:
            new_case = copy.deepcopy(case)
            # Replace query part: prompt = context + new_query
            context = case.get("context", "")
            new_case["prompt"] = context + "\n\n" + NEW_QUERIES[cid]
            new_cases.append(new_case)
        else:
            new_cases.append(case)

    # Save new benchmark
    output_benchmark = {"cases": new_cases}
    output_path = os.path.join(DEMO_DIR, "benchmark_all_v2.json")
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(output_benchmark, f, ensure_ascii=False, indent=2)
    print(f"Saved {len(new_cases)} cases to {output_path}")

    # Build new eval config
    new_constraints = {}
    for key, val in sorted(NEW_CONSTRAINTS.items()):
        new_constraints[key] = val

    # Stats
    total = len(new_constraints)
    hard = sum(1 for v in new_constraints.values() if v["type"] == "hard")
    soft = sum(1 for v in new_constraints.values() if v["type"] == "soft")

    case_counts = {}
    for key in new_constraints:
        cid = key.split("#")[0]
        case_counts[cid] = case_counts.get(cid, 0) + 1

    output_config = {
        "constraints": new_constraints,
        "stats": {
            "total": total,
            "hard": hard,
            "soft": soft,
            "cases": len(case_counts),
            "avg_per_case": round(total / len(case_counts), 1) if case_counts else 0,
        }
    }
    config_path = os.path.join(DEMO_DIR, "eval_config_v2.json")
    with open(config_path, "w", encoding="utf-8") as f:
        json.dump(output_config, f, ensure_ascii=False, indent=2)
    print(f"Saved {total} constraints ({hard} hard + {soft} soft) across {len(case_counts)} cases to {config_path}")
    print(f"Average: {total/len(case_counts):.1f} constraints per case")

    # Show cases without new queries
    all_case_ids = set(c["case_id"] for c in benchmark["cases"])
    covered = set(case_counts.keys())
    missing = all_case_ids - covered
    if missing:
        print(f"\nWARNING: {len(missing)} cases without new constraints: {sorted(missing)}")


if __name__ == "__main__":
    main()
